	//「２Ｄ描画」１フレーム毎に行う処理
	void  Object::Render2D_AF()
	{
		ML::Box2D src = { 0, 32, 32, 32 };
		ML::Box2D draw = { -16, -16, 32, 32 };
		draw.Offset(pos);
		DG::Image_Draw(res->imageName, draw, src);
		RenderFrameRed(hitBase);
	}

